package com.darklions.testmod.util.MathHelpers;

public class Vec3 
{
	private float x, y, z;
	
	public Vec3(float x, float y, float z)
	{
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public float X()
	{
		return this.x;
	}
	
	public float Y()
	{
		return this.y;
	}
	
	public float Z()
	{
		return this.z;
	}
}
